    <p>L’ULB accorde une grande importance à la protection de vos données à caractère personnel.</p>

    <p>En vous inscrivant sur le portail Hedrine de la Faculté de Pharmacie de l’Université Libre de Bruxelles (ci-après « ULB »),
        vous consentez à ce que les données à caractère personnel collectées lors de l’inscription (nom, prénom, adresse mail, numéro de téléphone, mot de passe crypté et nom de votre équipe/affiliation) soit hébergées dans la base de données Hedrine pour le bon fonctionnement de ce portail.
    </p>

    {{-- <p>Vos données sont transmises à l’Ordre des Pharmaciens de Belgique afin de procéder à une vérification de votre situation avant l’octroi de l’agrément.
        Sauf pour répondre à une obligation légale, elles ne sont pas transmises à des tiers.
    </p> --}}

    {{-- <p>De plus, une fois votre agrément obtenu, vos données pourront être utilisées par l’ULB pour communiquer avec vous à propos des étudiants accueillis dans votre officine,
        notamment pour vous inviter à leur cérémonie de proclamation.
    </p> --}}

    <p>Les membres du personnel de l’ULB ayant accès à vos donnés sont tenus à une obligation de confidentialité, celle-ci implique notamment de :</p>

    <ul>
        <li>ne pas accéder ou chercher à accéder à des données qui ne sont pas strictement nécessaires à l’exécution de leur fonction ;</li>
        <li>ne pas divulguer, tant au cours de leur relation de travail qu’après la fin de celle-ci, toute donnée à caractère personnel dont ils auraient eu connaissance dans l’exercice de leur fonction, outre ce qui s’avérerait nécessaire pour l’exercice de celle-ci ;</li>
        <li>ne conserver aucune copie des données traitées à l’issue de leur engagement à l’ULB.</li>
    </ul>

    {{-- <p>Les étudiants ayant accès à vos données sont tenus à des obligations de confidentialité similaires.</p> --}}
    <p>Voici ce qu'il en est par rapport à la suppression de vos données personnelles :</p>
    <ul>
        <li>Lors de la suppression d'un compte d'un lecteur (utilisateur qui ne modifie pas les données du site, mais les consulte), les données à caractère personnel seront supprimée immédiatement et définitivement de la base de données.</li>
        <li>Dans le cas où l'inscription d'un <b><u>lecteur</u></b> serait refusée par un administrateur, vos données seront supprimées du portail Hedrine dans les 30 jours suivant la notification du refus</li>
        <li>Il n'en est pas de même pour les personnes qui participent à la vie de ce portail en le mettant à jour les données (les utilisateurs qui modifient les données du site, donc qui ont ou auront un rôle de rédacteur, de publisher ou d'admin).  Vos données peronnelles ne pourront en effet pas être supprimées de la base de données, sous peine de dysfonctionnement important du portail.</li>
    </ul>    

    <p>Toutes vos questions sur la protection de vos données par l’ULB peuvent être envoyées à la Déléguée à la protection des données : rgpd@ulb.ac.be.</p>

    <p>Cette adresse est également celle par le biais de laquelle vous pouvez exercer vos droits en la matière, en justifiant votre identité : accès, rectification, effacement, limitation, opposition et portabilité.</p>

    <p>J’ai lu et j’affirme avoir compris dans son intégralité les dispositions relatives au traitement de mes données, que je sois ou devient un utilisateur de type lecteur, éditeur, publisher ou admin (RGPD).</p>
